(ns colors)


(defn color-string->palette [s]
  (into []
        (comp (map (fn [s]
                     (subs s 1)))
              (map #(partition 2 %))
              (map (fn [chars]
                     (mapv #(read-string (apply str "0x" %))
                           chars)))
              (map (fn [color]
                     (mapv #(/ % 255.0) color))))
        (clojure.string/split s #",")))

